public class Switch4
 {
    public static void main(String  args[])
    {
        int a=3;
        switch(a)

        {
            case 1+1:
            System.out.println("Inside case 1");
            break;
            case 2+1:
            System.out.println("Inside case 2");
            break;
            case 3+1:
            System.out.println("Inside case 3");
            break;
            default:
            System.out.println("Inside default");
        }
    }
    
}
