public class Constant2 
{
    public static void main(String args [])
    {
        int a=98;
        switch(a)
        {
            case 10:
            System.out.println("Inside case 10");
            break;
            case 'a':
            System.out.println("Inside case a");
            break;
            case 'b':
            System.out.println("Inside case b");
            break;
            default:
            System.out.println("Inside default");

        }
    }
}
