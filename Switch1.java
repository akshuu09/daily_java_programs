public class Switch1
 {
    public static void main(String args [])
    {
        int a=3;
        switch(a)
        {
            case 1:
            System.out.println("Inside in case 1");
            break;
            case 2:
            System.out.println("Inside in case 2");
            break;
            case 3:
            System.out.println("Inside in case 3");
            break;
            default:
            System.out.println("Inside in default case");
        }

    }
}
