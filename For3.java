//WAJP to check if num 3 is existing betweeen 1 to 5 

public class For3 
{
    public static void main(String args [])
    {
        for(int i=1; i<=5; i++)
        {
            System.out.println(i);
        
        if(i == 3)
        {
            System.out.println("the value of i is " +i);
            break;
        }
    }
    }
    
}
